public class Operadores {
    public static void main(String[] args) {
        
        /*  = (atribuição)
         *  == (igual)
         *  <=(menor igual)
         *  >= (maior igual)
         *  > (maior)
         *  < (menor)
         *  + (adicao)
         *  - (subtracao)
         *  / (divisao)
         *  * (multiplicacao)
         *  % (resto da divisao)
         */

         int num1 = 5;
         int num2 = 6;

         // = (atribuição)
         // num1 = num2;
         // == (igual)
         num1 == num2;
         // <=(menor igual)
         num1 <= num2;
         // >= (maior igual)
         num1 >= num2;

    }
    
}
